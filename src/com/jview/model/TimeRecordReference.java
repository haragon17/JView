package com.jview.model;

public class TimeRecordReference {

	private int tr_ref_id;
	private String tr_ref_name;
	private String tr_ref_kind;
	private String tr_ref_dept;
	
	public int getTr_ref_id() {
		return tr_ref_id;
	}
	public void setTr_ref_id(int tr_ref_id) {
		this.tr_ref_id = tr_ref_id;
	}
	public String getTr_ref_name() {
		return tr_ref_name;
	}
	public void setTr_ref_name(String tr_ref_name) {
		this.tr_ref_name = tr_ref_name;
	}
	public String getTr_ref_kind() {
		return tr_ref_kind;
	}
	public void setTr_ref_kind(String tr_ref_kind) {
		this.tr_ref_kind = tr_ref_kind;
	}
	public String getTr_ref_dept() {
		return tr_ref_dept;
	}
	public void setTr_ref_dept(String tr_ref_dept) {
		this.tr_ref_dept = tr_ref_dept;
	}
	
}
